    static func compileConfusionMatricesHTML(result: AnalysisResult) -> String {
        var html = ""
        let hasTest = result.confusionMatrix != nil
        let hasVal = result.valConfusionMatrix != nil
        
        if hasTest || hasVal {
            html += "<div class='grid-2 page-break'>"
            if hasTest {
                html += compileConfusionMatrixHTML(title: "Confusion Matrix (Test Set)", matrix: result.confusionMatrix)
            }
            if hasVal {
                html += compileConfusionMatrixHTML(title: "Confusion Matrix (Validation Set)", matrix: result.valConfusionMatrix)
            }
            html += "</div>"
        }
        return html
    }

    static func compileConfusionMatrixHTML(title: String, matrix: ConfusionMatrixData?) -> String {
        guard let matrix = matrix else { return "" }
        let labels = matrix.labels
        let values = matrix.values
        let maxVal = values.flatMap { $0 }.max() ?? 1
        
        var html = """
        <div class="card" style="break-inside: avoid;">
            <div class="card-title">🧩 \(title)</div>
            <div class="table-wrapper" style="margin-top: 15px;">
                <table style="border: none; margin: 0 auto; max-width: 450px; text-align: center;">
                    <thead>
                        <tr>
                            <th style="border: none; background: transparent;"></th>
                            <th colspan="\(labels.count)" style="text-align: center; border-bottom: 2px solid var(--indigo-accent); color: var(--purple-accent); font-weight: 700;">Predicted</th>
                        </tr>
                        <tr>
                            <th style="text-align: right; border-right: 2px solid var(--indigo-accent); color: var(--purple-accent); font-weight: 700; padding-right: 15px;">Actual</th>
        """
        for label in labels {
            html += "<th style='text-align: center; font-weight: 600;'>\(label)</th>"
        }
        html += """
                        </tr>
                    </thead>
                    <tbody>
        """
        for i in 0..<labels.count {
            html += "<tr>"
            html += "<td style='text-align: right; font-weight: 600; border-right: 2px solid var(--indigo-accent); padding-right: 15px; color: var(--text-secondary);'>\(labels[i])</td>"
            for j in 0..<labels.count {
                let val = (i < values.count && j < values[i].count) ? values[i][j] : 0
                let ratio = Double(val) / Double(maxVal)
                let bgOpacity = 0.08 + ratio * 0.45
                let textColor = ratio > 0.6 ? "#fff" : "var(--text-color)"
                let cellBorder = i == j ? "border: 2px solid var(--purple-accent);" : ""
                
                html += """
                <td style="text-align: center; font-weight: 700; background-color: rgba(99, 102, 241, \(bgOpacity)); color: \(textColor); \(cellBorder) padding: 12px; border-radius: 6px; font-size: 1.05rem;">
                    \(val)
                </td>
                """
            }
            html += "</tr>"
        }
        html += """
                    </tbody>
                </table>
            </div>
        </div>
        """
        return html
    }
    
    static func compileCorrelationMatrixHTML(result: AnalysisResult) -> String {
        guard !result.correlations.isEmpty else { return "" }
        
        // Extract unique variables in order
        var seen = Set<String>()
        var uniqueVars: [String] = []
        for pair in result.correlations {
            if !seen.contains(pair.x) {
                seen.insert(pair.x)
                uniqueVars.append(pair.x)
            }
            if !seen.contains(pair.y) {
                seen.insert(pair.y)
                uniqueVars.append(pair.y)
            }
        }
        
        // Cap to top 15 variables to prevent table overflow on A4 pages
        let varsToDraw = Array(uniqueVars.prefix(15))
        guard !varsToDraw.isEmpty else { return "" }
        
        // Build lookup
        var correlationLookup: [String: Double] = [:]
        for pair in result.correlations {
            let key1 = "\(pair.x)|\(pair.y)"
            let key2 = "\(pair.y)|\(pair.x)"
            correlationLookup[key1] = pair.value
            correlationLookup[key2] = pair.value
        }
        
        var html = """
        <div class="card page-break" style="break-inside: avoid;">
            <div class="card-title">📊 Feature Correlation Matrix</div>
            <div class="table-wrapper" style="margin-top: 15px; overflow-x: auto;">
                <table style="border: none; table-layout: fixed; width: 100%; border-collapse: collapse; text-align: center; font-size: 0.75rem;">
                    <thead>
                        <tr>
                            <th style="border: none; background: transparent; width: 120px;"></th>
        """
        for v in varsToDraw {
            html += "<th style='text-align: center; font-weight: 600; padding: 4px; overflow: hidden; text-overflow: ellipsis; white-space: nowrap;'>\(v)</th>"
        }
        html += """
                        </tr>
                    </thead>
                    <tbody>
        """
        for rowVar in varsToDraw {
            html += "<tr>"
            html += "<td style='text-align: right; font-weight: 600; padding: 6px 10px; border-right: 2px solid var(--indigo-accent); overflow: hidden; text-overflow: ellipsis; white-space: nowrap; color: var(--text-secondary); width: 120px;'>\(rowVar)</td>"
            for colVar in varsToDraw {
                let val: Double
                if rowVar == colVar {
                    val = 1.0
                } else {
                    let key = "\(rowVar)|\(colVar)"
                    val = correlationLookup[key] ?? 0.0
                }
                
                let absVal = abs(val)
                let cellColor: String
                let textColor: String
                if val == 1.0 {
                    cellColor = "rgba(99, 102, 241, 0.85)"
                    textColor = "#fff"
                } else if absVal < 0.01 {
                    cellColor = "rgba(255, 255, 255, 0.02)"
                    textColor = "rgba(255, 255, 255, 0.3)"
                } else if val > 0 {
                    cellColor = "rgba(59, 130, 246, \(0.1 + absVal * 0.75))"
                    textColor = absVal > 0.5 ? "#fff" : "var(--text-color)"
                } else {
                    cellColor = "rgba(239, 68, 68, \(0.1 + absVal * 0.75))"
                    textColor = absVal > 0.5 ? "#fff" : "var(--text-color)"
                }
                
                let cellBorder = rowVar == colVar ? "border: 1px solid var(--purple-accent);" : ""
                let displayVal = rowVar == colVar ? "1.0" : (absVal > 0.01 ? String(format: "%.2f", val) : "—")
                
                html += """
                <td style="text-align: center; font-weight: 700; background-color: \(cellColor); color: \(textColor); \(cellBorder) padding: 8px 4px; font-size: 0.75rem;">
                    \(displayVal)
                </td>
                """
            }
            html += "</tr>"
        }
        html += """
                    </tbody>
                </table>
            </div>
            <div style="margin-top: 10px; font-size: 0.75rem; color: var(--text-secondary); text-align: center; display: flex; justify-content: center; gap: 15px;">
                <span><span style="display: inline-block; width: 10px; height: 10px; background-color: rgba(59, 130, 246, 0.8); border-radius: 2px;"></span> Positive correlation</span>
                <span><span style="display: inline-block; width: 10px; height: 10px; background-color: rgba(239, 68, 68, 0.8); border-radius: 2px;"></span> Negative correlation</span>
            </div>
        </div>
        """
        return html
    }

    static func compileCorrelationsSectionHTML(result: AnalysisResult) -> String {
        guard !result.correlations.isEmpty else { return "" }
        
        let hasMatrix = result.confusionMatrix != nil || result.valConfusionMatrix != nil
        let pageBreakClass = hasMatrix ? "" : "page-break"
        
        var html = "<div class='\(pageBreakClass)'>"
        html += "<div class='card' style='break-inside: avoid;'>"
        html += "<div class='card-title'>🔗 Top Feature Correlations</div>"
        html += "<div class='table-wrapper'><table>"
        html += "<thead><tr><th>Feature A</th><th>Feature B</th><th>Correlation Coefficient</th><th>Strength</th></tr></thead>"
        html += "<tbody>"
        for corr in result.correlations.prefix(10) {
            let absVal = abs(corr.value)
            let badgeClass: String
            let strengthText: String
            if absVal >= 0.7 {
                badgeClass = "badge-warning"
                strengthText = "Strong"
            } else if absVal >= 0.4 {
                badgeClass = "badge-info"
                strengthText = "Moderate"
            } else {
                badgeClass = "badge-info"
                strengthText = "Weak"
            }
            
            html += "<tr>"
            html += "<td><code>\(corr.x)</code></td>"
            html += "<td><code>\(corr.y)</code></td>"
            html += "<td style='font-weight: 600; color: \(corr.value >= 0 ? "#34d399" : "#f87171")'>\(String(format: "%.3f", corr.value))</td>"
            html += "<td><span class='badge \(badgeClass)'>\(strengthText)</span></td>"
            html += "</tr>"
        }
        html += "</tbody></table></div></div></div>"
        return html
    }

