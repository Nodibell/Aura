    static func buildMarkdownReport(result: AnalysisResult, narrative: String?, includeStats: Bool = true, includeCharts: Bool = true, includeTable: Bool = false) -> String {
        var md = ""
        if let narrative = narrative {
            md += narrative + "\n\n---\n\n"
        }
        md += "# Analysis Report: \(result.targetColumn)\n\n"
        md += "_Generated by Aura — \(Date().formatted(date: .long, time: .shortened))_\n\n"
        md += "---\n\n"

        if includeStats {
            md += "## 📊 Dataset Overview\n\n"
            md += "| Metric | Value |\n|---|---|\n"
            md += "| Rows | \(result.rowCount) |\n"
            md += "| Columns | \(result.colCount) |\n"
            md += "| Task Type | \(result.taskType.capitalized) |\n"
            md += "| Target Column | `\(result.targetColumn)` |\n"
            md += "| Numeric Columns | \(result.numericColCount) |\n"
            md += "| Categorical Columns | \(result.categoricalColCount) |\n\n"

            md += "### 🤖 Model Performance\n\n"
            md += "| Model | \(result.metrics.scoreType) |\n|---|---|\n"
            for model in result.modelsCompared {
                md += "| \(model.name) | \(String(format: "%.4f", model.score)) |\n"
            }
            md += "\n"

            let missingCols = result.missingValues.filter { $0.value > 0 }
            if !missingCols.isEmpty {
                md += "### ⚠️ Missing Values\n\n"
                md += "| Column | Missing Count |\n|---|---|\n"
                for (col, count) in missingCols.sorted(by: { $0.value > $1.value }) {
                    md += "| `\(col)` | \(count) |\n"
                }
                md += "\n"
            }
        }

        if includeCharts && !result.charts.isEmpty {
            md += "## 📈 Charts\n\n"
            for chart in result.charts {
                md += "### \(chart.title)\n\n"
                md += "_\(chart.xLabel) → \(chart.yLabel)_\n\n"
                md += "| \(chart.xLabel) | \(chart.yLabel) |\n|---|---|\n"
                for point in chart.data.prefix(20) {
                    let x = point.xVal ?? (point.xNum.map { String(format: "%.4f", $0) } ?? "—")
                    md += "| \(x) | \(String(format: "%.6f", point.y)) |\n"
                }
                if chart.data.count > 20 {
                    md += "\n_… \(chart.data.count - 20) more points not shown_\n"
                }
                md += "\n"
            }
        }

        if includeTable, let fp = result.fullPreview {
            md += "## 🗃️ Data Sample (first 50 rows)\n\n"
            md += "| " + fp.columns.joined(separator: " | ") + " |\n"
            md += "|" + fp.columns.map { _ in "---" }.joined(separator: "|") + "|\n"
            for row in fp.rows.prefix(50) {
                md += "| " + row.joined(separator: " | ") + " |\n"
            }
            md += "\n"
        }

        if !result.correlations.isEmpty {
            md += "## 🔗 Top Correlations\n\n"
            md += "| Feature A | Feature B | Correlation |\n|---|---|---|\n"
            for corr in result.correlations.prefix(10) {
                md += "| `\(corr.x)` | `\(corr.y)` | \(String(format: "%.3f", corr.value)) |\n"
            }
            md += "\n"
        }

        if result.confusionMatrix != nil || result.valConfusionMatrix != nil {
            md += "## 🧩 Confusion Matrices\n\n"
            if let cm = result.confusionMatrix {
                md += "### Confusion Matrix (Test Set)\n\n"
                md += "| Actual \\ Predicted | " + cm.labels.joined(separator: " | ") + " |\n"
                md += "|---| " + cm.labels.map { _ in "---" }.joined(separator: "|") + " |\n"
                for i in 0..<cm.labels.count {
                    var rowStr = "| **\(cm.labels[i])**"
                    for j in 0..<cm.labels.count {
                        let val = i < cm.values.count && j < cm.values[i].count ? cm.values[i][j] : 0
                        rowStr += " | \(val)"
                    }
                    md += rowStr + " |\n"
                }
                md += "\n"
            }
            if let cm = result.valConfusionMatrix {
                md += "### Confusion Matrix (Validation Set)\n\n"
                md += "| Actual \\ Predicted | " + cm.labels.joined(separator: " | ") + " |\n"
                md += "|---| " + cm.labels.map { _ in "---" }.joined(separator: "|") + " |\n"
                for i in 0..<cm.labels.count {
                    var rowStr = "| **\(cm.labels[i])**"
                    for j in 0..<cm.labels.count {
                        let val = i < cm.values.count && j < cm.values[i].count ? cm.values[i][j] : 0
                        rowStr += " | \(val)"
                    }
                    md += rowStr + " |\n"
                }
                md += "\n"
            }
        }

        if !result.correlations.isEmpty {
            md += "## 📊 Feature Correlation Matrix\n\n"
            var seen = Set<String>()
            var uniqueVars: [String] = []
            for pair in result.correlations {
                if !seen.contains(pair.x) { seen.insert(pair.x); uniqueVars.append(pair.x) }
                if !seen.contains(pair.y) { seen.insert(pair.y); uniqueVars.append(pair.y) }
            }
            let varsToDraw = Array(uniqueVars.prefix(10))
            if !varsToDraw.isEmpty {
                var correlationLookup: [String: Double] = [:]
                for pair in result.correlations {
                    correlationLookup["\(pair.x)|\(pair.y)"] = pair.value
                    correlationLookup["\(pair.y)|\(pair.x)"] = pair.value
                }
                
                md += "| Feature | " + varsToDraw.joined(separator: " | ") + " |\n"
                md += "|---| " + varsToDraw.map { _ in "---" }.joined(separator: "|") + " |\n"
                for rowVar in varsToDraw {
                    var rowStr = "| `\(rowVar)`"
                    for colVar in varsToDraw {
                        if rowVar == colVar {
                            rowStr += " | 1.00"
                        } else {
                            let key = "\(rowVar)|\(colVar)"
                            if let val = correlationLookup[key] {
                                rowStr += String(format: " | %.2f", val)
                            } else {
                                rowStr += " | —"
                            }
                        }
                    }
                    md += rowStr + " |\n"
                }
                md += "\n"
            }
        }

        return md
    }

